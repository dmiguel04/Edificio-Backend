def generate_aes_key() -> str:
    """Generar una clave AES-256 válida en base64"""
    import secrets
    key_bytes = secrets.token_bytes(32)  # 32 bytes = 256 bits
    return base64.b64encode(key_bytes).decode()

def get_or_create_aes_key() -> bytes:
    """Obtener o crear clave AES válida"""
    # Clave por defecto válida para desarrollo (32 bytes)
    default_key = "MTIzNDU2Nzg5MDEyMzQ1NjEyMzQ1Njc4OTAxMjM0NTY="  # "123456789012345612345678901234567" en base64
    
    AES_KEY_B64 = get_env_variable("AES_KEY_B64", default_key)
    
    try:
        key_bytes = base64.b64decode(AES_KEY_B64)
        if len(key_bytes) != 32:
            if DEBUG:
                # En desarrollo, generar clave válida automáticamente
                print(f"⚠️  Clave AES inválida ({len(key_bytes)} bytes). Generando clave válida...")
                new_key = generate_aes_key()
                print(f"💡 Nueva clave AES generada: {new_key}")
                print(f"💡 Agrega esta variable a tu .env: AES_KEY_B64={new_key}")
                return base64.b64decode(new_key)
            else:
                raise ValueError(f"AES key debe ser de 32 bytes, recibido: {len(key_bytes)} bytes")
        
        return key_bytes
        
    except Exception as e:
        if DEBUG:
            print(f"⚠️  Error decodificando AES key: {e}")
            print("💡 Usando clave por defecto para desarrollo...")
            return base64.b64decode(default_key)
        else:
            raise EnvironmentError(f"Error decodificando AES_KEY_B64: {e}")

