def get_env_variable(var_name: str, default_value: str = None, required: bool = False) -> str:
    """
    Obtener variable de entorno con validación y valores por defecto
    """
    value = os.environ.get(var_name, default_value)
    if required and not value:
        raise EnvironmentError(f"Variable de entorno requerida no encontrada: {var_name}")
    return value

